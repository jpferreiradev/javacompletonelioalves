package EstruturaSequencial.atividades;

public class Atividade01 {
    public static void main(String[]args){

        int numeroUm;
        int numeroDois;
        int primeiraSoma;
        int segundaSoma;
        int terceiraSoma;

        numeroUm = 10;
        numeroDois = 30;
        primeiraSoma = numeroUm + numeroDois;

        numeroUm = -30;
        numeroDois = 10;
        segundaSoma = numeroUm + numeroDois;

        numeroUm = 0;
        numeroDois = 0;
        terceiraSoma = numeroUm + numeroDois;



        System.out.println("SOMA = " + primeiraSoma);
        System.out.println("SOMA = " + segundaSoma);
        System.out.println("SOMA = " + terceiraSoma);
    }
}
