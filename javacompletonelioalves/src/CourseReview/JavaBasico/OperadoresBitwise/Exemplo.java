package CourseReview.JavaBasico.OperadoresBitwise;

public class Exemplo {
    public static void main(String[] args) {

        int n1 = 89;
        int n2 = 60;

        System.out.println(n1 & n2); // E
        System.out.println(n1 | n2); //  Ou
        System.out.println(n1 ^ n2); // Ou exclusivo


    }
}
