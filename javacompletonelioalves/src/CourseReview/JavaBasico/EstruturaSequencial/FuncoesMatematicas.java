package CourseReview.JavaBasico.EstruturaSequencial;

public class FuncoesMatematicas {
    public static void main(String[] args) {

        int x = 3;
        double y = 4.0;
        double a = 5.0;

        // Raiz quadrada
        System.out.println("Raiz quadrada");
        System.out.println(Math.sqrt(x));
        System.out.println();

        // Elevado ao quadrado
        System.out.println("Elevado ao quadrado");
        System.out.println(Math.pow(y,2));
        System.out.println();

        // Valor absoluto, é quando é tirado o sinal caso exista
        System.out.println("Valor absoluto, é quando é tirado o sinal caso exista");
        System.out.println(Math.abs(a));
        System.out.println();


    }
}
