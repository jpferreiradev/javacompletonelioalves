package CourseReview.OrientacaoObjetos.membrosEstatiscos.util;

public class CalculatorFinal {

    public static final double PI = 3.14159;

    public static double valorCincunferencia(double raio) {
        return 2.0 * PI * raio;
    }

    public static double valorVolume(double raio) {
        return 4.0 * PI * raio * raio * raio / 3.0;
    }

}

