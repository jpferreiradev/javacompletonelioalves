package courseOld.OutrosTopicosJava;

public class OperadoresBitwise {
    public static void main(String[] args) {

        int n1 = 89;
        int n2 = 60;

        System.out.println(n1 & n2); // e
        System.out.println(n1 | n2); // ou
        System.out.println(n1 ^ n2); // ou exclusivo

    }
}
